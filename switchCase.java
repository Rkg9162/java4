public class switchCase {
    private static final String Rakesh = null;

    public static void main(String[] args) {
        String Name = "Rakesh";
        switch(Name)
        {
            case "Rakesh" : System.out.println("this is true name ");
            break;
            case "Rahul" : System.out.println("this is false name ");
            break;
            case "amit" : System.out.println("this is false name ");
            break;
        }
    }
    
}
